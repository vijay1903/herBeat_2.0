    var fbOpts = {
        clientID: FB_APP_ID,
        clientSecret: FB_APP_SECRET,
        callbackURL: 'http://localhost:8080/auth/facebook/callback',
        passwordField: ['email']
    };

    // var fbCallback = function(accessToken, refreshToken, profile, cb){
